# ejemplo de concatenar o sumar valores
num1_cadena = "10"
print(f"Numero 1 en cadena: {num1_cadena}")

num2_cadena = "20"
print(f"Numero 1 en cadena: {num2_cadena}")

resultado = num1_cadena + num2_cadena
print(f"Concatenar: {resultado}")

# conversion
num1_int = int(num1_cadena)
num2_int = int(num2_cadena)
resultado_int = num1_int + num2_int
print(f"Sumar: {resultado_int}")
