a, b = 10, 3
print(f"Valor inicial a: {a}, b: {b}")

# operador compuesto suma (+=)
a += b # a = a + b
print(f"a += b = {a}")
a = 10 # reiniciamos valor de a

# operador compuesto resta (-=)
a -= b # a = a- b
print(f"a -= b = {a}")
a = 10 # reiniciamos valor de a

# operador compuesto multiplicacion (*=)
a *= b # a = a * b
print(f"a *= b = {a}")
a = 10 # reiniciamos valor de a

# operador compuesto division (/=)
a /= b # a = a / b
print(f"a /= b = {a:.2f}")